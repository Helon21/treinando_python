#calculo da área de um ciruclo

pi = 3.14

raio = float(input("Informe o valor do raio"))

area = pi * raio

print("A constante de pi já foi inserida na equação, seu valor corresponde á ",pi)
print("\n")
print("O circulo que possui o raio equivalente à {}, possui área que corresponde ao valor {}".format(raio,area))